void adxl_write (uint8_t reg, uint8_t value)
{
	uint8_t data[2];
	data[0] = reg;
	data[1] = value;
	HAL_I2C_Master_Transmit (&hi2c1, adxl_address, data, 2, 100);
}



void adxl_read_values (uint8_t reg)
{
	HAL_I2C_Mem_Read (&hi2c1, adxl_address, reg, 1, (uint8_t *)data_rec, 6, 100);
}

void adxl_read_address (uint8_t reg)
{
	HAL_I2C_Mem_Read (&hi2c1, adxl_address, reg, 1, &chipid, 1, 100);
}

void adxl_init (void)
{
	adxl_read_address (0x00); // read the DEVID

	adxl_write (0x31, 0x00);  // data_format range= +- 2g
	adxl_write (0x2d, 0x00);  // reset all bits
	adxl_write (0x2d, 0x08);  // power_cntl measure and wake up 8hz

}

void ReadI2CSensor()
{
							adxl_read_values (0x32);
	  x = ((data_rec[1]<<8)|data_rec[0]);
	  y = ((data_rec[3]<<8)|data_rec[2]);
	  z = ((data_rec[5]<<8)|data_rec[4]);

	  xg = fabs(x * .003906);
	  yg = fabs(y * .003906);
	  zg = fabs(z * .003906);
}

void saveI2CSample()
{	
	DataBufferX[sample_no]=xg;
	DataBufferY[sample_no]=yg;
	DataBufferZ[sample_no]=zg;
	sample_no+=1;
}

void ProcessData()
{
	int i;
	float sumX, sumY, sumZ;
	
	sumX=0;
	sumY=0;
	sumZ=0;
	
	for(int i=0;i<bufferlen;i++)
	{
		sumX+=pow(DataBufferX[i],2);
		sumY+=pow(DataBufferY[i],2);
		sumZ+=pow(DataBufferZ[i],2);
	}
	
	RMSX=sqrt(sumX/bufferlen);
	RMSY=sqrt(sumY/bufferlen);
	RMSZ=sqrt(sumZ/bufferlen);
}

void DisplayInfo()
{	
	#ifndef DISABLE_DISPLAY
	static char string_display[30];
	
	// Display I2C sensor redings
	ssd1306_SetCursor(0, 0);
	
		
	// Display analog sensors readings
	ssd1306_SetCursor(0, 0); // ssd1306_SetCursor(0, 14);
	sprintf(string_display,"Xrms=%.3fg    ",RMSX);
	ssd1306_WriteString(string_display,Font_11x18, White);
	ssd1306_SetCursor(0, 22); // ssd1306_SetCursor(0, 14);
	sprintf(string_display,"Yrms=%.3fg    ",RMSY);
	ssd1306_WriteString(string_display,Font_11x18, White);
  ssd1306_SetCursor(0, 44); // ssd1306_SetCursor(0, 14);
  sprintf(string_display,"Zrms=%.3fg    ",RMSZ);
  ssd1306_WriteString(string_display,Font_11x18, White);
	
	// Copy all data from local screenbuffer to the screen
  ssd1306_UpdateScreen(&hi2c1);
	#endif
	
}

void SendToPC()
{
	sprintf((char*) TxBuffer, "\n x = %.3f g \t y = %.3f g \t z = %.3f g ", xg, yg, zg);
	HAL_UART_Transmit(&huart4, TxBuffer, sizeof(TxBuffer), 100);
}

